    public void bindDateCombo() {
        dateCombo.removeAllItems();
        
        ArrayList<String> timeStamps = HelperMethods.getListOfTimeStamp(member.getVitalSignHistory());
        ArrayList<String> dates = new ArrayList<>();
        for (String stamp : timeStamps) {
            String date = HelperMethods.getDateFromStamp(stamp) + " 00:00:00";
            
            ArrayList<String> status = new ArrayList<>();
            status.add(Constants.REQUEST_PENDING);
            
            ArrayList<WorkRequest> pendingRequestOnDate = userAccount.getWorkQueue().searchRequestByDate(HelperMethods.getDateForString(date, true), status);
            
            if (!dates.contains(date) && pendingRequestOnDate.isEmpty()) {
                dates.add(HelperMethods.getDateFromStamp(date));
            }
        }
        
        for (String date : dates) {
            dateCombo.addItem(date);
        }
    }
