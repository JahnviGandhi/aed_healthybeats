    private void requestBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestBtnActionPerformed
        // TODO add your handling code here:
        if (validateInput()) {
            String date = (String) dateCombo.getSelectedItem();
            HealthCheckRequest request = new HealthCheckRequest();
            
            request.setMessage(msgTxt.getText().trim());
            request.setRequestDate(new Date());
            request.setSender(userAccount);
            request.setStatus(Constants.REQUEST_PENDING);
            request.setVitalSignsList(member.getVitalSignHistory().getVitalSignsForDate(date));
            request.setFcdList(member.getFoodConsumptionHistory().getFoodConsumptionForDate(date));
            
            userAccount.getWorkQueue().addWorkRequest(request);
            organization.getWorkQueue().addWorkRequest(request);
                    
            for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (org instanceof HealthCareOrganization) {
                    org.getWorkQueue().addWorkRequest(request);
                    break;
                }
            }
            
            JOptionPane.showMessageDialog(null, "Health Check requested successfully!", "Information", JOptionPane.INFORMATION_MESSAGE);
            bindDateCombo();
            msgTxt.setText("");
            if (dateCombo.getItemCount() <= 0) {
                JOptionPane.showMessageDialog(null, "No new health check can be requested!", "Warning", JOptionPane.WARNING_MESSAGE);
                requestBtn.setEnabled(false);
            }
        }
    }//GEN-LAST:event_requestBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        requestHealthCheckPanel.populateRequestTable((ArrayList) userAccount.getWorkQueue().getWorkRequestList());
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed
    
    public boolean validateInput() {
        if (dateCombo.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(null, "Please select a date to request for health check!", "Error", JOptionPane.ERROR_MESSAGE);
            dateCombo.requestFocus();
            return false;
        }
        else if (msgTxt.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a Message!", "Error", JOptionPane.ERROR_MESSAGE);
            msgTxt.requestFocus();
            return false;
        }
        else {
            return true;
        }
    }
    

