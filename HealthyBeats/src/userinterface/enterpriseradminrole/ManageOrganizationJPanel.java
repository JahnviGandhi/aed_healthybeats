    public void populateOrgTable(ArrayList<Organization> orgList) {
        DefaultTableModel dtm = (DefaultTableModel) organizationTable.getModel();
        dtm.setRowCount(0);
        
        for (Organization org : orgList) {
            Object row[] = new Object[2];
            row[0] = org.getOrganizationId();
            row[1] = org;
            
            dtm.addRow(row);
        }
    }
    
    public void bindOrgTypeCombo() {
        orgTypeCombo.removeAllItems();
        ArrayList<String> orgList = new ArrayList<>();
        
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            orgList.add(org.getOrganizationName());
        }
        
        for (OrganizationType type: OrganizationType.values()) {
            if (!orgList.contains(type.getValue())) {
                orgTypeCombo.addItem(type);
            }
        }
        
        if (orgTypeCombo.getItemCount() == 0) {
            addOrgBtn.setEnabled(false);
            errorMsgLbl.setText("All types of Organizations are created for the Enterprise!");
            errorMsgLbl.setVisible(true);
        }
        else {
            addOrgBtn.setEnabled(true);
            errorMsgLbl.setVisible(false);
        }
    }
    
