    private void addOrgBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrgBtnActionPerformed
        // TODO add your handling code here:
        if (validateInput()) {
            Organization org = enterprise.getOrganizationDirectory().createOrganization((OrganizationType) orgTypeCombo.getSelectedItem());
            
            if (org != null) {
                JOptionPane.showMessageDialog(null, "Organization created successfully!", "Information", JOptionPane.INFORMATION_MESSAGE);
                refresh(enterprise.getOrganizationDirectory().getOrganizationList());
            }
        }
    }//GEN-LAST:event_addOrgBtnActionPerformed

    private void deleteOrgBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteOrgBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = organizationTable.getSelectedRow();
        
        if (selectedRow >= 0) {
            int confirmButton = JOptionPane.YES_NO_OPTION;
            
            int confirmDelete = JOptionPane.showConfirmDialog(null, "Would you like to delete selected network?", "Warning", confirmButton);
            
            if (confirmDelete == JOptionPane.YES_OPTION) {
                Organization org = (Organization) organizationTable.getValueAt(selectedRow, 1);
                enterprise.getOrganizationDirectory().deleteOrganization(org);
                
                populateOrgTable(enterprise.getOrganizationDirectory().getOrganizationList());
            }  
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a row to delete organization!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteOrgBtnActionPerformed

    public boolean validateInput() {
        if (orgTypeCombo.getSelectedIndex() < 0) {
            JOptionPane.showMessageDialog(null, "Please select a type to add organization!", "Error", JOptionPane.ERROR_MESSAGE);
            orgTypeCombo.requestFocus();
            return false;
        }
        else {
            return true;
        }
    }

    public void refresh(ArrayList<Organization> orgList) {
        bindOrgTypeCombo();
        populateOrgTable(orgList);
        searchTxt.setText("");
    }
    
