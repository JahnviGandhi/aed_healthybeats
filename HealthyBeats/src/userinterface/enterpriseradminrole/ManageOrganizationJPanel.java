    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        String searchInput = searchTxt.getText().trim();

        if (!searchInput.isEmpty()) {
            ArrayList<Organization> orgs = enterprise.getOrganizationDirectory().searchOrganization(searchInput);

            if (orgs.size() > 0) {
                populateOrgTable(orgs);
            }
            else {
                JOptionPane.showMessageDialog(null, "No such organization exists!", "Error", JOptionPane.ERROR_MESSAGE);
                refresh(enterprise.getOrganizationDirectory().getOrganizationList());
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Please enter Enterprise Name to perform Search!", "Warning", JOptionPane.WARNING_MESSAGE);
            searchTxt.requestFocus();
        }
    }//GEN-LAST:event_searchBtnActionPerformed

