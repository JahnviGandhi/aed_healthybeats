    public void populateProdTable() {
        DefaultTableModel dtm = (DefaultTableModel) productCatalogTable.getModel();
        dtm.setRowCount(0);
        
        for (Product prod : store.getProductCatalog().getProductCatalog()) {
            Object row[] = new Object[5];
            row[0] = prod.getBarcodeNumber();
            row[1] = prod;
            row[2] = prod.getWeight();
            row[3] = prod.getQuantity();
            row[4] = prod.getPrice();
            
            dtm.addRow(row);
        }
    }
    
