    private void deleteProdBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteProdBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = productCatalogTable.getSelectedRow();
        
        if (selectedRow >= 0) {
            int confirmButton = JOptionPane.YES_NO_OPTION;
            
            int confirmDelete = JOptionPane.showConfirmDialog(null, "Would you like to delete selected store admin?", "Warning", confirmButton);
            
            if (confirmDelete == JOptionPane.YES_OPTION) {
                Product product = (Product) productCatalogTable.getValueAt(selectedRow, 1);
                
                store.getProductCatalog().removeProduct(product);
                populateProdTable();
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a row to delete Product!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteProdBtnActionPerformed

