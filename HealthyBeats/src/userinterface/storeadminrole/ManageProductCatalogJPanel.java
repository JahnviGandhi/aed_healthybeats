    private void viewDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = productCatalogTable.getSelectedRow();
        
        if (selectedRow >= 0) {
            Product product = (Product) productCatalogTable.getValueAt(selectedRow, 1);
            
            ViewProductDetailsJPanel viewProdDetailPanel = new ViewProductDetailsJPanel(userProcessContainer, store, product, this);
            userProcessContainer.add("viewProdDetailPanel", viewProdDetailPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a row to view details!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_viewDetailsBtnActionPerformed


