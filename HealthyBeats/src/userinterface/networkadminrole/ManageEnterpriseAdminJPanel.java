    private void addEnterpriseAdminBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEnterpriseAdminBtnActionPerformed
        // TODO add your handling code here:
        if(validateInput()) {
            Enterprise enterprise = (Enterprise) enterpriseCombo.getSelectedItem();
            
            if (enterprise != null) {
                Person person = new Person(); 
                person.setFirstName(firstNameTxt.getText().trim());
                person.setLastName(lastNameTxt.getText().trim());
                person.setContactNo(contactNoTxt.getText().trim());
                
                enterprise.getPersonDirectory().addPerson(person);
                
                if(maleRadioBtn.isSelected()) {
                    person.setGender("Male");
                }
                else {
                    person.setGender("Female");
                }
                
                UserAccount ua = enterprise.getUserAccountDirectory().createUserAccount(usernameTxt.getText().trim(), String.valueOf(pwdTxt.getPassword()), person, new EnterpriseAdminRole());
                
                if (ua != null) {
                    JOptionPane.showMessageDialog(null, "Enterprise Admin Account created successfully!", "information", JOptionPane.INFORMATION_MESSAGE);
                    refresh();
                }
            }
            else {
                JOptionPane.showMessageDialog(null, "Please select an enterprise to add Admin!", "Warning", JOptionPane.WARNING_MESSAGE);
                enterpriseCombo.requestFocus();
            }
        }
    }//GEN-LAST:event_addEnterpriseAdminBtnActionPerformed

    private void deleteAdminBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAdminBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterpriseAdminTable.getSelectedRow();
        
        if (selectedRow >= 0) {
            int confirmButton = JOptionPane.YES_NO_OPTION;
            
            int confirmDelete = JOptionPane.showConfirmDialog(null, "Would you like to delete selected enterprise admin?", "Warning", confirmButton);
            
            if (confirmDelete == JOptionPane.YES_OPTION) {
                UserAccount ua = (UserAccount) enterpriseAdminTable.getValueAt(selectedRow, 4);
                Enterprise enterprise = (Enterprise) enterpriseAdminTable.getValueAt(selectedRow, 1);
                
                enterprise.getUserAccountDirectory().removeUserAccount(ua);
                enterprise.getPersonDirectory().removePerson(ua.getPerson());
                
                refresh();
            }  
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a row to delete Network Admin!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteAdminBtnActionPerformed

