    public void populateEnterpriseAdminTable() {
        DefaultTableModel dtm = (DefaultTableModel) enterpriseAdminTable.getModel();
        dtm.setRowCount(0);
        
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            
            if (enterprise.getUserAccountDirectory().getUserAccountDirectory().size() > 0) {
                Object row[] = new Object[5];
                row[0] = enterprise.getOrganizationId();
                row[1] = enterprise;
                row[2] = enterprise.getEnterpriseType().getValue();
                row[3] = enterprise.getUserAccountDirectory().getUserAccountDirectory().get(0).getPerson().getFirstName();
                row[4] = enterprise.getUserAccountDirectory().getUserAccountDirectory().get(0);
            
                dtm.addRow(row);
            }
        }
    }
    
    public void bindEnterpriseCombo() {
        enterpriseCombo.removeAllItems();
        
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (enterprise.getUserAccountDirectory().getUserAccountDirectory().isEmpty()) {
                enterpriseCombo.addItem(enterprise);
            }
        }
        
        if (enterpriseCombo.getItemCount() == 0) {
            addEnterpriseAdminBtn.setEnabled(false);
            errorMsgLbl.setText("All Enterprises are assigned an Enterprise Admin!");
            errorMsgLbl.setVisible(true);
        }
        else {
            errorMsgLbl.setVisible(false);
            addEnterpriseAdminBtn.setEnabled(true);
        }
    }
    
