    private void addNetworkAdminBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNetworkAdminBtnActionPerformed
        // TODO add your handling code here:
        if (validateInput()) {
            Network network = (Network) networkComboBox.getSelectedItem();
            
            if (network != null) {
                Person person = new Person();
                person.setFirstName(firstNameTxt.getText().trim());
                person.setLastName(lastNameTxt.getText().trim());
                person.setContactNo(contactNoTxt.getText().trim());
                
                network.getPersonDirectory().addPerson(person);
                
                if(maleRadioBtn.isSelected()) {
                    person.setGender("Male");
                }
                else {
                    person.setGender("Female");
                }
                
                UserAccount ua = network.getUserAccountDirectory().createUserAccount(usernameTxt.getText().trim(), String.valueOf(pwdTxt.getPassword()), person, new NetworkAdminRole());
                
                if (ua != null) {
                    JOptionPane.showMessageDialog(null, "Network Admin Account created successfully!", "information", JOptionPane.INFORMATION_MESSAGE);
                    refresh();
                }
            }
            else { 
                JOptionPane.showMessageDialog(null, "Please select a network to add Admin!", "Warning", JOptionPane.WARNING_MESSAGE);
                networkComboBox.requestFocus();
            }
        }
    }//GEN-LAST:event_addNetworkAdminBtnActionPerformed

    private void viewDetailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = networkAdminTable.getSelectedRow();
        
        if (selectedRow >= 0) {
            UserAccount ua = (UserAccount) networkAdminTable.getValueAt(selectedRow, 3);
            Network network = (Network) networkAdminTable.getValueAt(selectedRow, 1);
            
            ViewNetworkAdminDetailsJPanel viewAdminDetailsPanel = new ViewNetworkAdminDetailsJPanel(userProcessContainer, network, ua, this);
            userProcessContainer.add("viewNetworkAdminDetailPanel", viewAdminDetailsPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a row to view details!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_viewDetailsBtnActionPerformed

    private void deleteNetworkAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteNetworkAdminActionPerformed
        // TODO add your handling code here:
        int selectedRow = networkAdminTable.getSelectedRow();
        
        if (selectedRow >= 0) {
            int confirmButton = JOptionPane.YES_NO_OPTION;
            
            int confirmDelete = JOptionPane.showConfirmDialog(null, "Would you like to delete selected network admin?", "Warning", confirmButton);
            
            if (confirmDelete == JOptionPane.YES_OPTION) {
                UserAccount ua = (UserAccount) networkAdminTable.getValueAt(selectedRow, 3);
                Network network = (Network) networkAdminTable.getValueAt(selectedRow, 1);
                
                network.getUserAccountDirectory().removeUserAccount(ua);
                network.getPersonDirectory().removePerson(ua.getPerson());
                
                refresh();
            }  
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a row to delete Network Admin!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteNetworkAdminActionPerformed

