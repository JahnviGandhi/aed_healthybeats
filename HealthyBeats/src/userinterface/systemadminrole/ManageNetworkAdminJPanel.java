    public void populateNetworkAdminTable() {
        DefaultTableModel dtm = (DefaultTableModel) networkAdminTable.getModel();
        dtm.setRowCount(0);
        
        for (Network network : system.getNetworkList().getNetworkList()) {
            
            if (network.getUserAccountDirectory().getUserAccountDirectory().size() > 0) {
                Object row[] = new Object[4];
                row[0] = network.getOrganizationId();
                row[1] = network;
                row[2] = network.getUserAccountDirectory().getUserAccountDirectory().get(0).getPerson().getFirstName();
                row[3] = network.getUserAccountDirectory().getUserAccountDirectory().get(0);
            
                dtm.addRow(row);
            }
        }
    }
    
    public void bindNetworkCombo() {
        networkComboBox.removeAllItems();
        
        for (Network network : system.getNetworkList().getNetworkList()) {
            if (network.getUserAccountDirectory().getUserAccountDirectory().isEmpty()) {
                networkComboBox.addItem(network);
            }
        }
        
        if (networkComboBox.getItemCount() == 0) {
            addNetworkAdminBtn.setEnabled(false);
            errorMsgLbl.setText("All networks are assigned a Network Admin!");
            errorMsgLbl.setVisible(true);
        }
        else {
            errorMsgLbl.setVisible(false);
            addNetworkAdminBtn.setEnabled(true);
        }
    }
    
